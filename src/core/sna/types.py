    total_checks: int = 0
    total_triggered: int = 0
    total_alerts: int = 0
    total_dispatched: int = 0
    total_notified: int = 0
    active_monitors: int = 0
    active_alerts: int = 0
    scheduler_state: SchedulerState = SchedulerState.STOPPED
    last_check_time: float = 0.0
    scheduler_uptime_seconds: float = 0.0


