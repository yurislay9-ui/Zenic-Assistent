# ──────────────────────────────────────────────────────────────
#  ENUMS
# ──────────────────────────────────────────────────────────────

class MonitorWeight(str, Enum):
    """Classification of monitor computational weight."""
    LIGHTWEIGHT = "lightweight"   # <100ms, simple DB queries
    MEDIUM = "medium"             # 100ms-1s, aggregations
    HEAVY = "heavy"               # >1s, multi-source analysis


class AlertSeverity(str, Enum):
    """Severity level for SNA alerts."""
    INFO = "info"
    WARNING = "warning"
    CRITICAL = "critical"


class AlertStatus(str, Enum):
    """Lifecycle status of an alert."""
    PENDING = "pending"       # Awaiting DAG dispatch
    DISPATCHED = "dispatched" # Sent to DAG pipeline
    NOTIFIED = "notified"     # User notified successfully
    ACKNOWLEDGED = "acknowledged"  # User acknowledged
    RESOLVED = "resolved"     # Condition resolved
    EXPIRED = "expired"       # Alert TTL expired


class ThresholdOperator(str, Enum):
    """Comparison operator for threshold evaluation."""
    GT = "gt"           # value > threshold
    GTE = "gte"         # value >= threshold
    LT = "lt"           # value < threshold
    LTE = "lte"         # value <= threshold
    EQ = "eq"           # value == threshold
    NEQ = "neq"         # value != threshold
    BETWEEN = "between" # low <= value <= high


class SchedulerState(str, Enum):
    """State of the SNA scheduler."""
    STOPPED = "stopped"
    RUNNING = "running"
    PAUSED = "paused"
    ERROR = "error"


